    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String idProducto = txtIdProducto.getText();

        if (idProducto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, ingresa un ID válido.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int id = Integer.parseInt(idProducto);

            ProductoDAO productoDAO = new ProductoDAO();
            Producto editarProducto = productoDAO.obtenerPorId(id);

            if (editarProducto == null) {
                JOptionPane.showMessageDialog(null, "No se encontró el producto con el ID ingresado.", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (txtNombre.getText().isEmpty() || txtDescripcion.getText().isEmpty()
                    || txtPrecio.getText().isEmpty() || txtCantidad.getText().isEmpty()
                    || txtStock.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Todos los campos deben estar llenos.", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String nombre = txtNombre.getText();
            String descripcion = txtDescripcion.getText();
            double precio = Double.parseDouble(txtPrecio.getText());
            int cantidad = Integer.parseInt(txtCantidad.getText());
            int stock = Integer.parseInt(txtStock.getText());

            Producto productoEditado = new Producto(editarProducto.getIdColeccion(), nombre, descripcion, precio, cantidad, stock);

            boolean consultaEditar = productoDAO.actualizar(productoEditado);

            if (consultaEditar) {
                JOptionPane.showMessageDialog(null, "El producto ha sido editado correctamente.");

                // Limpiar y desactivar los campos
                txtNombre.setEditable(false);
                txtDescripcion.setEditable(false);
                txtPrecio.setEditable(false);
                txtCantidad.setEditable(false);
                txtStock.setEditable(false);

                txtNombre.setText("");
                txtDescripcion.setText("");
                txtPrecio.setText("");
                txtCantidad.setText("");
                txtStock.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo actualizar el producto.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, ingresa valores numéricos válidos en los campos de precio, cantidad y stock.", "Error", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrió un error inesperado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String idProducto = txtIdProducto.getText();

        if (idProducto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, ingresa un ID válido.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int id = Integer.parseInt(idProducto);

            ProductoDAO productoDAO = new ProductoDAO();
            Producto editarProducto = productoDAO.obtenerPorId(id);

            if (editarProducto == null) {
                JOptionPane.showMessageDialog(null, "No se encontró el producto con el ID ingresado.", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Habilitar los campos para edición
            txtNombre.setEditable(true);
            txtDescripcion.setEditable(true);
            txtPrecio.setEditable(true);
            txtCantidad.setEditable(true);
            txtStock.setEditable(true);

            txtNombre.setText(editarProducto.getNombre());
            txtDescripcion.setText(editarProducto.getDescripcion());
            txtPrecio.setText(String.valueOf(editarProducto.getPrecio()));
            txtCantidad.setText(String.valueOf(editarProducto.getCantidad()));
            txtStock.setText(String.valueOf(editarProducto.getStock()));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, ingresa un ID numérico válido.", "Error", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrió un error inesperado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

