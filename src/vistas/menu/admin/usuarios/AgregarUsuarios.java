    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreCompleto = txtNombre.getText().trim();
        String correo = txtCorreo.getText().trim();
        String password = txtContrase単a.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String direccion = txtDireccion.getText().trim();
        String rol = (String) comboBoxRol.getSelectedItem();

        if (nombreCompleto.isEmpty() || correo.isEmpty() || password.isEmpty() || telefono.isEmpty() || direccion.isEmpty() || rol == null) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Usuario nuevoUsuario = new Usuario(nombreCompleto, correo, password, telefono, direccion);
        nuevoUsuario.setRol(rol);

        UsuariosDAO usuariosDAO = new UsuariosDAO();
        boolean consultaRegistrar = false;
        try {
            consultaRegistrar = usuariosDAO.insertar(nuevoUsuario);
        } catch (Exception ex) {
            Logger.getLogger(AgregarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (consultaRegistrar) {
            JOptionPane.showMessageDialog(null, "Usuario registrado con 辿xito.");
            txtNombre.setText("");
            txtCorreo.setText("");
            txtContrase単a.setText("");
            txtTelefono.setText("");
            txtDireccion.setText("");
            comboBoxRol.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "Hubo un error al registrar el usuario.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

